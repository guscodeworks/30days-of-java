package day1;

public class TiposPrimitivos {
    //Numeros inteiros
    byte a = 10;
    short b = 15;
    int c = 20;
    long d = 25L;
    //Ponto Flutuantes
    float e = 30f; //f no final necessario para Java indentificar que é o tipo float
    double f = 35; //"padrao" da linguagem Java

    //Booleanos
    char g = 'B'; //Definido com aspas simples
    boolean h = true || false; //verdadeiro ou falso
    //Escrita
    String nome = "Gustavo"; //definido sempre com aspas duplas
}
